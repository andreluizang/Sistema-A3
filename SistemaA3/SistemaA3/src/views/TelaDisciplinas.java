    private void renomearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_renomearButtonActionPerformed
        String antigaDisciplina = disciplinasList.getSelectedValue();
        String novaDisciplina = JOptionPane.showInputDialog("Digite o novo nome da disciplina: ");
        boolean cadastrada = false;
        DisciplinaDAO disciplinadao = new DisciplinaDAO();
        if(!DisciplinaDAO.disciplina.isEmpty()){
            for(int i = 0; i < DisciplinaDAO.disciplina.size(); i++){
                if(novaDisciplina.equals(DisciplinaDAO.disciplina.get(i).getNome())){
                    cadastrada = true;
                    break;
                }else{
                    cadastrada = false;
                }
            }
        }
        if(cadastrada){
            JOptionPane.showMessageDialog(null, "Essa disciplina já está cadastrada!", "ERRO!", 0);
            System.out.println("Essa disciplina já está cadastrada!");
        }else{
            conexao.getConnection();
            int id = 0;
            int localId = disciplinasList.getSelectedIndex();
            String query = "SELECT id FROM DISCIPLINA WHERE nome = ? AND fk_estudante = ?";
            ArrayList<Object> dados = new ArrayList<>();
            dados.add(disciplinasList.getSelectedValue()); dados.add(EstudanteDAO.estudante.getId());
            ResultSet rs = conexao.Select(query, dados);
            try{
                if(rs!=null && rs.next()){
                    id = rs.getInt(1);
                }
            }catch(SQLException ex){
                Logger.getLogger(ConexaoDAO.class.getName()).log(Level.SEVERE, null, ex);            
            }
            conexao.closeConnection();
            disciplinadao.atualizarDisciplina(id, novaDisciplina, antigaDisciplina, localId);
        }
        disciplinasList.setModel(new javax.swing.DefaultComboBoxModel<>(disciplinadao.formatarList()));
        
    }//GEN-LAST:event_renomearButtonActionPerformed

